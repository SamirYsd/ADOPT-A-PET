    <p>Dogs are pack animals—they depend on you for love and protection, so keep them inside, take them for walks, and make them a part of your family.</p>
    <h3>How to train them?</h3>
    <p>You and your dogs speak different languages. Humane, interactive training gives dogs greater freedom and a better understanding of our world. Untrained dogs are often punished for their “improper” behavior. Be the one to train your dog—you are the one who will need to know how to communicate with him or her—but get help from a humane dog trainer if you run into problems.

        Compassion, clarity, and consistency are the most important elements of dog training. Training should not include any activity or device that endangers animals (e.g., electric shock collars) or puts undue stress on them.</p>

    <h3>How many times a day should I walk my dog?</h3>
    <p>Puppies should be taken out at least once every two hours (or within a half hour after eating or drinking) and guided to the same spot where they can smell having relieved themselves before. Immediately after they “do their business,” they should be praised lavishly in a high-pitched, excited voice. Crate training does not speed up the housebreaking process: Puppies do not develop full bladder control before 6 months of age and are physically incapable of “holding it” for very long.

        Take your adult pooch out at least four times a day and allow him or her time to linger and smell the fire hydrants. If you can’t go home at lunchtime, enlist the help of a neighbor or professional dog-walker.</p>
    
    <h3>Social life</h3>
    <p>Animals get depressed if deprived of adequate social interaction, so let them “chat” and play with other dogs on walks and at parks. A happy dog’s life is not one long series of commands: Let them live a little, make choices, and take their time. Animals are very sensitive and can become easily frightened, so speak softly to dogs. Don’t fight or shout in their presence, and give them their own hiding places to dash into when they want to be alone.</p>

    <h3>Feeding</h3>
    <p>If you have been feeding your dog commercial pet foods, you may be jeopardizing his or her health. Cheap, supermarket pet foods are often composed of ground-up parts of animals deemed by Agriculture Department inspectors unfit for human consumption. Many guardians feed healthful, meatless diets to their dogs.

        Feed dogs first, before you eat. To keep dogs from begging at the dinner table, teach them the simple phrase “All done!” (You must be consistent with this—absolutely no more food can be given after saying the phrase, or the meaning will be lost.) After they understand “All done!” you will be able to feed your dogs something from the table without becoming the target of any begging after you stop</p>


    

    


